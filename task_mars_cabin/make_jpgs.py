import struct
import os

def make_jpg(path):
    hdr = bytes([0xFF, 0xD8, 0xFF, 0xE0])
    hdr += b'JFIF\x00'
    hdr += bytes([0x01, 0x01, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00])
    hdr += struct.pack('>HH', 0xFF, 0xDB)
    hdr += bytes([0x00, 0x84, 0x00])
    hdr += bytes([0x10] * 132)
    hdr += struct.pack('>HH', 0xFF, 0xC0)
    hdr += bytes([0x00, 0x0B, 0x08, 0x00, 0x01, 0x00, 0x01, 0x01, 0x01, 0x11, 0x00])
    hdr += struct.pack('>HH', 0xFF, 0xC4)
    hdr += bytes([0x00, 0x1B, 0x00, 0x00, 0x00, 0x00, 0x00])
    hdr += bytes([0x00] * 20)
    hdr += struct.pack('>HH', 0xFF, 0xDA)
    hdr += bytes([0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F, 0x00, 0x37, 0x7F, 0xFF, 0xD9])
    with open(path, 'wb') as f:
        f.write(hdr)

